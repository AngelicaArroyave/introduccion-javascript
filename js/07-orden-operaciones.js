// Orden de las operaciones

let result

result = 20 + 30 * 2
console.log(result); // 80

result = (20 + 30) * 2
console.log(result); // 100

result = (100 + 200 + 300) * .2
console.log(result); // 120

// Incrementos

let score = 10

score++
console.log(score); // 11

++score
console.log(score); // 12

score += 10
console.log(score); // 20

// Decremento

score--
console.log(score); // 11

--score
console.log(score); // 10
